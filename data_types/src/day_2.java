public class day_2 {
    public static  void main (String arg[]){
        // Boolean type
        boolean isSet = true; // it can be true or false.
        System.out.println(isSet);

        // Textual type
        char ch1 = 'a';
        System.out.println(ch1);

        // Integral data type
        byte b1 = 127;
        System.out.println(b1);
        short s1 = 32767;
        System.out.println(s1);
        int i1 = 456787867;
        System.out.println(i1);
        long l1 = 45678967823456789l; // use l at the end cz the range is greater than int range
        System.out.println(l1);

        // floating points data type
        float f1 = 1.5f;
        System.out.println(f1);
        double d1 = 5.5;
        System.out.println(d1);

        // Object type
        String str1 = "Hello";
        System.out.println(str1);
        int i2 = str1.length();


    }
}
